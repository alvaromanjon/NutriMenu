package app.objects;

public class GroupUnitObj {

	public String group;

	public String unit;

	public GroupUnitObj(String group, String unit) {
		this.group = group;
		this.unit = unit;

	}

	public GroupUnitObj() {

	}

	public String getGroup() {
		return group;
	}

	public void setGroup(String group) {
		this.group = group;
	}

	public String getUnit() {
		return unit;
	}

	public void setUnit(String unit) {
		this.unit = unit;
	}

}
